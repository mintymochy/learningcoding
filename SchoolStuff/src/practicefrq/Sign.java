public class Sign {
	private String str;
	private int x;

	public Sign(String str, int x) {
		this.str = str;
		this.x = x;
	}

	public String getSTR() {
		return str;
	}

	public void setSTR(String str) {
		this.str = str;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

}
